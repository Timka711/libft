char* ft_strrchr(char *a, int n)
{
	int i;
	int res;
	res = -1;
	i = 0;
	while (a[i] != '\0')
	{
		if (a[i] == n)
			res = i;
		i++;
	}
	if (res == -1)
		return (0);
	else
		return (&a[res]);
}
