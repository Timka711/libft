#include "libft.h"

char	*strdup(const char *s)
{
	int		i;
	char	*s1;

	s1 = (char *)malloc(ft_strlen(s) + 1);
	if (s1 == 0)
		return (0);
	i = -1;
	while (s[++i] != '\0')
		s1[i] = s[i];
	return (s1);
}
