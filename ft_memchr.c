#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*s1;

	s1 = (char *)s;
	i = -1;
	while (++i < n)
		if (s1[i] == c)
			return (&s1[i]);
	return (0);
}
