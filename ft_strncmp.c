int ft_strncmp(char *a, char *b, int n)
{
	int i;
	i = 0;
	while (a[i] == b[i])
	{
		i++;
	}
	if (a[i] > b[i])
		return (1);
	else if (a[i] < b[i])
		return (-1);
	else
		return (0);
}
