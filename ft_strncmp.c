#include <stdlib.h>

int	ft_strncmp(char *a, char *b, int n)
{
	int	i;

	i = 0;
	while (a[i] == b[i] && i < n)
 		i++;
	if (a[i] > b[i])
		return (1);
	else if (a[i] < b[i])
		return (-1);	
	return (0);
}
