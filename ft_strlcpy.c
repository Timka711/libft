size_t ft_strlcpy(char *buf, const char *a, int n)
{
	int i;
	i = 0;
	while (a[i] != '\0')
	{
		if (i < n - 1)
			buf[i] = a[i];
		i++;
	}
	buf[n - 1] = '\0';
	return (i);
}
