#include "libft.h"

size_t	ft_strlcpy(char *buf, const char *a, size_t n)
{
	size_t	i;

	i = 0;
	if (!buf)
		return (0);
	if (n > 0)
		buf[i] = '\0';
	while (a[i] != '\0')
	{
		if (i + 1 < n)
		{
			buf[i] = a[i];
			buf[i + 1] = '\0';
		}
		i++;
	}
	return (i);
}
