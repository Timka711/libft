#include <stdlib.h>

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((char *)s1)[i] = ((char *)s2)[i];
		i++;
		if (((char *)s2)[i - 1] == c)
			return (s1);
	}
	return (0);
}
