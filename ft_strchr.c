#include <stdlib.h>

char	*ft_strchr(const char *a, int n)
{
	int	i;

	i = 0;
	while (a[i] != '\0')
	{
		if (a[i] == n)
			return (&((char *)a)[i]);
		i++;
	}
	return (0);
}
